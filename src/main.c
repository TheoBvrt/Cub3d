void	drawline(int xOrigin, int yStart, int yEnd, t_game *game) {
	int x;
	int y;

	x = xOrigin;
	y = yStart;
	while (y <= yEnd)
	{
		mlx_pixel_put(game->mlx, game->mlx_win, x, y, 0xFFFFFF);
		y ++;
	}
}

int main(int argc, char *argv[])
{
	t_game		game;

	if (cube3d_init(&game) == 1)
		return (1);
	game.mlx = mlx_init();
	game.mlx_win = mlx_new_window(game.mlx, game.screenWidth, game.screenHeight, "Cub3D");
	draw_ground(&game);
	draw_sky(&game);
	raycasting(&game);
	mlx_loop(game.mlx);
	return (0);
}
